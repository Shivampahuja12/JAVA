public class ControlFlow {
    public static void main(String[] args) {
        int score = 90;
        if (score >= 90){
            System.out.println("You got an A!");
        }
        else if (score >= 80){
            System.out.println("You got a B!");
        }
        else {
            System.out.println("You need to improve");
        }
    }
}
